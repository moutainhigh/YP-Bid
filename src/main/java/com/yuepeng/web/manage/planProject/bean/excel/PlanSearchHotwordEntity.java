package com.yuepeng.web.manage.planProject.bean.excel;

import cn.afterturn.easypoi.excel.annotation.Excel;

import java.util.Date;

/**
 * @Description: 搜索热词excel
 * @Author: ZhongShengbin
 * @Date: 2020/05/18 16:00
 * Copyright (c) 2019, Samton. All rights reserved
 */
public class PlanSearchHotwordEntity {

    @Excel(name = "热词内容", width = 20)
    private String hotwordName;

    @Excel(name = "搜索次数", width = 20)
    private Integer searchTimes;

    @Excel(name = "新增时间", width = 25, format = "yyyy-MM-dd HH:mm:ss")
    private Date createDate;

    public String getHotwordName() {
        return hotwordName;
    }

    public void setHotwordName(String hotwordName) {
        this.hotwordName = hotwordName;
    }

    public Integer getSearchTimes() {
        return searchTimes;
    }

    public void setSearchTimes(Integer searchTimes) {
        this.searchTimes = searchTimes;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }
}
